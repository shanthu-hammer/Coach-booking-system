    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String dbURL = "jdbc:mysql://localhost:3306/busbooking";
        String usernamee = "root";
        String passwordd = "1234567890";
        try {
            String Username = empId.getText();
            String Password = empPass.getText();
            try {
                Class.forName("com.mysql.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(EmployeeLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
            Connection conn = DriverManager.getConnection(dbURL, usernamee, passwordd);
            String sql = "select *from empoyeetable where email=? && password=?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, Username);
            stmt.setString(2, Password);
            ResultSet rs = stmt.executeQuery();
             

            if (rs.next()) {
               JOptionPane.showMessageDialog(null, "Login Successful");
               
                EmployeeManage next = new EmployeeManage();
                next.setVisible(true);
                this.dispose();
            } else {
                 JOptionPane.showMessageDialog(null, "Invalid Username or Password");
            
             
            }
             } catch (SQLException ex) {
            Logger.getLogger(EmployeeLogin.class.getName()).log(Level.SEVERE, null, ex);
    }//GEN-LAST:event_jButton1ActionPerformed
    }
